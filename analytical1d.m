%Analytical Solution
T = 40000;
g = 980;
w = 1/80;
c = sqrt(T*g/w);
u = zeros(1,9);
disp('_________________________________________________________________________________________________________________________________')
disp('     t            0           10           20           30           40           50           60           70           80 ')
disp('_________________________________________________________________________________________________________________________________')
%Calculate
t = 0;
rev = 0;%revolution check
while rev <=1
    for i = 2:8
    x = (i-1)*10;
    u(i) = .5*(oddf(x-(c*t))+oddf(x+(c*t)));%Calculate u(i)
    end
    if t >= -1
        rev = 2;
    end
    fprintf(' %7.4f      %7.4f      %7.4f      %7.4f      %7.4f      %7.4f      %7.4f      %7.4f       %7.4f     %6.3f\n',t,u(1),u(2),u(3),u(4),u(5),u(6),u(7),u(8),u(9));
    t = t+.00005;
end
xarr = [0 10 20 30 40 50 60 70 80];
plot(xarr,u)